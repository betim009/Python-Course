# Tópico 2: Variáveis e Tipos de Dados
# Exemplo de declaração de variáveis e tipos de dados em Python.
nome = "João"           # Variável do tipo string
idade = 25              # Variável do tipo inteiro
altura = 1.75           # Variável do tipo float
is_estudante = True     # Variável do tipo booleano

# Exibindo os valores das variáveis na tela.
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("É estudante?", is_estudante)
